export const getCzechCoinPrice = (year, value) => {
    // Value input might be float (1, 0.5) or string ("1 Kč")
    // We expect clean float or string that matches keys
    let vKey = typeof value === 'number' ? value.toFixed(2) : parseFloat(value).toFixed(2);

    // Normalize .1 to .10 etc if needed, but toFixed(2) handles it (0.10)
    // Check if key exists
    if (!CZECH_PRICES[vKey]) {
        // Fallback or retry?
        return 0;
    }

    const priceData = CZECH_PRICES[vKey];
    const yStr = year.toString();

    if (priceData[yStr]) {
        return priceData[yStr];
    }

    return priceData['default'] || 0;
};
