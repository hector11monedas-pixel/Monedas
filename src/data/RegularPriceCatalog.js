export const getRegularCoinPrice = (country, year, value, mint = null) => {
    if (!REGULAR_PRICES[country]) return null;
    if (!REGULAR_PRICES[country][year]) return null;

    let valKey = parseFloat(value).toFixed(2);

    // Support mint variants (e.g., '2002-S') stored as '0.01_S' or similar
    if (mint) {
        const variantKey = `${valKey}_${mint}`;
        if (REGULAR_PRICES[country][year][variantKey]) {
            return REGULAR_PRICES[country][year][variantKey];
        }
    }

    if (REGULAR_PRICES[country][year][valKey]) {
        return REGULAR_PRICES[country][year][valKey];
    }

    return null;
};
